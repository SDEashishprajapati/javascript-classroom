
// Switch statement (calculator)
let operator = "*";
let x = 6, y = 3;
switch (operator) {
    case "+":
        console.log(x + y);
        break;
    case "-":
        console.log(x - y);
        break;
    case "*":
        console.log(x * y);
        break;
    case "/":
        console.log(x / y);
        break;
    default:
        console.log("Invalid operator");
}
